    private void searchBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBActionPerformed
        // TODO add your handling code here:
        
        
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/food_management","root","");
            //String s = "SELECT itemname, price from testinsert where itemcode = ?";           
            String s =  "select food_name, price from foods where food_id =?";
            PreparedStatement ps = con.prepareStatement(s);
            ps.setString(1,itemcode.getText());
            ResultSet rs = ps.executeQuery();
            
            
            
            if(rs.next()==false)
                //itemname.setText(rs.getString("itemname"));
                
                JOptionPane.showMessageDialog(this, "Invalid Item Code. Try again");       
            
            else{
                
                itemname.setText((rs.getString("food_name")));
                price.setText((rs.getString("price")));
                //JOptionPane.showMessageDialog(this, "saved successfully");
            }
            
        }        
        catch(Exception e){
            JOptionPane.showMessageDialog(this,e);
                   
        }
        
        
    }//GEN-LAST:event_searchBActionPerformed

