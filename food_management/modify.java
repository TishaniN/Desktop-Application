     public boolean  verifyFields()
        {
        String iname = newname.getText();
        String iprice = newprice.getText();       
        
        if(iname.trim().equals("")||iprice.trim().equals("")){
            
            JOptionPane.showMessageDialog(null, "One or More Fields are Empty!");
            return false;
        }
        else{
            return true;
        }
        }
    
    
    
